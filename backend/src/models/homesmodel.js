import mongoose from "mongoose";

const HomesSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    location: {
        type: String,
        required: true,
    },
});

const HouseModel = mongoose.model("House", HomesSchema);

export default HouseModel;
